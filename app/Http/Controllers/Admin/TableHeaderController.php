    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $tableheader = new \App\Models\TableHeader;

        $tableheader->label = $request->label;
        $tableheader->name = $request->name;
        $tableheader->type = $request->type;

        $tableheader->options = '';

        if(count($request->options)) {
            $tableheader->options = serialize($request->options);    
        }

        $tableheader->default = $request->default;
        $tableheader->display_order = $request->display_order;

        $tableheader->save();

        return redirect()->back();

    }

